public class ForEachloop {
    public static void main(String[] args) {
      
              String[] str = { "hello", "hi" };
              System.out.println(str);
              for (int i = 0; i < str.length; i++) {
                System.out.println(str[i]);
              }
          
              // For each loop
              for (String i : str) {
                System.out.println(i);
              }
          
            }
          }
    
    

